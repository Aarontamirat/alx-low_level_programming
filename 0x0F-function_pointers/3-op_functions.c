#include "3-calc.h"

/**
 * op_add - sum calculator
 * @a: integer
 * @b: integer
 * Return: sum
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - difference calculator
 * @a: integer
 * @b: integer
 * Return: subtraction
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - product calculator
 * @a: integer
 * @b: integer
 * Return: product
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - division calculator
 * @a: integer
 * @b: integer
 * Return: division
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - remainder calculator
 * @a: integer
 * @b: integer
 * Return: remainder
 */

int op_mod(int a, int b)
{
	return (a % b);
}
